 _  _  _  _  _  _  _  _  _ 
 _| _| _| _| _| _| _| _| _|
|_ |_ |_ |_ |_ |_ |_ |_ |_ 
       
    ),
      [3,3,3,3,3,3,3,3,3] => %Q(
 _  _  _  _  _  _  _  _  _   
 _| _| _| _| _| _| _| _| _|
 _| _| _| _| _| _| _| _| _|
    ),
      [4,4,4,4,4,4,4,4,4] => %Q(
                           
|_||_||_||_||_||_||_||_||_|
  |  |  |  |  |  |  |  |  |
    ),
      [5,5,5,5,5,5,5,5,5] => %Q(
 _  _  _  _  _  _  _  _  _ 
|_ |_ |_ |_ |_ |_ |_ |_ |_ 
 _| _| _| _| _| _| _| _| _|
       
    ),
