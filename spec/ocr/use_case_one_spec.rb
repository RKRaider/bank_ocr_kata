| || || || || || || || || |
|_||_||_||_||_||_||_||_||_|
       
    ),
      [1,1,1,1,1,1,1,1,1] => %Q(
                           
  |  |  |  |  |  |  |  |  |
  |  |  |  |  |  |  |  |  |

