 _  _  _  _  _  _  _  _  _ 
|_ |_ |_ |_ |_ |_ |_ |_ |_ 
|_||_||_||_||_||_||_||_||_|
    ),
      [7,7,7,7,7,7,7,7,7] => %Q(
 _  _  _  _  _  _  _  _  _ 
  |  |  |  |  |  |  |  |  |
  |  |  |  |  |  |  |  |  |
    ),
      [8,8,8,8,8,8,8,8,8] => %Q(
 _  _  _  _  _  _  _  _  _ 
|_||_||_||_||_||_||_||_||_|
